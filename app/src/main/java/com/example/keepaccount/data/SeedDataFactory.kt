    private fun seedRecord(
        type: BillType,
        category: Int,
        amountCents: Long,
        note: String,
        date: LocalDate,
        time: LocalTime,
        zoneId: ZoneId,
    ): BillRecordEntity {
        val occurredAt = date.atTime(time).atZone(zoneId).toInstant().toEpochMilli()
        return BillRecordEntity(
            id = 0,
            type = type,
            category = category,
            amountCents = amountCents,
            note = note,
            occurredAt = occurredAt,
            createdAt = occurredAt,
            updatedAt = occurredAt,
        )
    }

    private fun julyExpense(
        categoryName: String,
        amountCents: Long,
        date: LocalDate,
        time: LocalTime,
        zoneId: ZoneId,
        note: String = "",
    ): BillRecordEntity =
        seedRecord(
            type = BillType.EXPENSE,
            category = DefaultCategories.idOf(categoryName),
            amountCents = amountCents,
            note = note,
            date = date,
            time = time,
            zoneId = zoneId,
        )

    private fun expenseAmount(dayIndex: Int, variant: Int): Long {
        val base = 600L + ((dayIndex * 37 + variant * 53) % 9600)
        return base + if (variant == 0) 0L else 350L
    }

    private fun incomeAmount(dayIndex: Int): Long {
        return 380000L + (dayIndex % 12) * 45000L
    }
}
